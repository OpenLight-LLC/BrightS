int brights_font_char_width(char c)
{
  (void)c;
  return FONT_WIDTH;
}

int brights_font_string_width(const char *str)
{
  if (!str) return 0;
  int width = 0;
  while (*str) {
    width += FONT_WIDTH;
    str++;
  }
  return width;
}

void brights_font_draw_char(int x, int y, char c, uint32_t fg_color, uint32_t bg_color)
{
  if (!brights_fb_available()) return;
  
  const uint8_t *glyph = brights_font_8x16[(unsigned char)c];
  
  brights_fb_info_t *fb = brights_fb_get_info();
  uint32_t *framebuffer = (uint32_t *)fb->framebuffer;
  uint32_t stride = fb->pitch / 4;
  
  for (int row = 0; row < 16; row++) {
    uint8_t bits = glyph[row];
    for (int col = 0; col < 8; col++) {
      int px = x + (7 - col);
      int py = y + row;
      if (px >= 0 && px < (int)fb->width && py >= 0 && py < (int)fb->height) {
        if (bits & (1 << col)) {
          framebuffer[py * stride + px] = fg_color;
        } else if (bg_color != 0xFFFFFFFF) {
          framebuffer[py * stride + px] = bg_color;
        }
      }
    }
  }
}

void brights_font_draw_string(int x, int y, const char *str, uint32_t fg_color, uint32_t bg_color)
{
  if (!str) return;
  
  int cx = x;
  while (*str) {
    brights_font_draw_char(cx, y, *str, fg_color, bg_color);
    cx += FONT_WIDTH;
    str++;
  }
}
